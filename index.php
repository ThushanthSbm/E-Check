B.	Mask Detection<br>
A deep learning-based approach for recognizing masks over faces in public places is provided in order to reduce Coronavirus community spread. Using an ensemble of single and two stage detectors, the suggested model efficiently manages various types of occlusions in crowded situations. The ensemble technique not only aids in reaching high accuracy but also significantly increases detection speed. Face mask detection employs the visual stream from the camera in conjunction with AI algorithms to recognize and notify persons who are not wearing face masks. A user-friendly interface enables monitoring and assessment of system alarms.
