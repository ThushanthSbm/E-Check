                   A.	Thermal and sickness  detection<br>
During previous infectious disease outbreaks, such as COVID-19, infrared thermography scanners were frequently employed as a screening method. This approach, which is focused on measuring absolute body temperature to screen for fever, lacks specificity for COVID-19. If they are concerned about the repercussions of missing coming to work, some people may take fever-reducing medicine. Relying just on temperature screening will not prevent the spread of COVID-19 at work.
Thermal screening in the workplace can be regarded part of a bundle of COVID-19 prevention and control measures. Workers should be encouraged to self-monitor their health, maybe via surveys, and to measure their own temperature at home on a regular basis. That is why we have proposed a sickness detection system along with the thermal detection system.
The sickness detection system will use facial malformations to detect whether a person is sick or not. This will be done using the raspberry pi camera while thermal detection will be done using thermal camera as shown below in the system diagram.
Here, at first before adding the cameras, the models will be trained with data sets separately with thermal image data and facial malformation data to detect the sickness. After the model training, the models will be integrated with the raspberry pi. Then the camera inputs will be processed and accurate data will be delivered. The accuracy of the device will be tested using test cases.
